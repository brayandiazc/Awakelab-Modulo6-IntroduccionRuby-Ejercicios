array = [1, 2, 3, 4, 5]

print array.shuffle # array.shuffle mezcla el contenido del array.
print array.uniq    # array.uniq quita los valores duplicados.
print array.compact       # array.compact, quita los ceros de la matriz.
print array.flatten       # array.flatten, convierte una matriz en un array.
print array.include?(2)   # array.include?(2), busca un valor en el array.
print array.delete_at(1)  # array.delete_at(1), elimina el valor en la posici√≥n 1.
print array.delete('c')   # array.delete('c'), elimina el valor 'c' del array.
print [1, 2, 3, 4].join(',') # [1,2,3,4].join(','), convierte el array en una cadena.
